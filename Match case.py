a = int(input("Enter a day number : "))

match a:
    case 0:
        print("Monday")
    case 1:
        print("Tuesday")
    case 2:
        print("Wednesday")
    case 3:
        print("thursday")
    case 4:
        print("Friday")
    case 5:
        print("saturday")
    case 6:
        print("Sunday")
    case _ :
        print("Wrong day number")




